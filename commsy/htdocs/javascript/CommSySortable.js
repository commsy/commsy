function initSortable(id){
   var item_id = id;
   window.addEvent('domready', function(){
      new Sortables($(item_id), {
         initialize: function(){
	        var step = 0;
		    this.elements.each(function(element, i){
		       step = step + 5;
			   element.setStyle('height', '40');
		    });
	     }

      });
   });
}